#Даны три переменные вещественного типа: A, B, C. Если их значения упорядочены по возрастанию, то удвоить их; в противном случае заменить значение каждой переменной на противоположное. Вывести новые значения переменных A, B,
# Ввод значений A, B, C
A = float(input("Введите значение A: "))
B = float(input("Введите значение B: "))
C = float(input("Введите значение C: "))

# Проверка, упорядочены ли значения по возрастанию
if A <= B <= C:
    A *= 2
    B *= 2
    C *= 2
else:
    A = -A
    B = -B
    C = -C

# Вывод новых значений переменных
print(f"Новые значения: A = {A}, B = {B}, C = {C}")








